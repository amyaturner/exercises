let weather = "sunny";

switch (weather) {
  case 'sunny':
    console.log("It's a beautiful, sunny day!");
    break;
  case 'rainy':
    console.log("Grab your umbrella.");
    break;
  case 'snowy':
    console.log("Burr! No way!");
    break;
  default:
    console.log("It's all good.");
}

